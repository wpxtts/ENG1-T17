package com.mygdx.game;

public class ComponentPosition {

    // Contains all the variables which determine the size
    // and position of an object..

    float x;
    float y;
    float height;
    float width;

    ComponentPosition(float x, float y, float height, float width) {
        this.x = x;
        this.y = y;
        this.height = height;
        this.width = width;
    }

}
